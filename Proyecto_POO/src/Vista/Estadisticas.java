    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (jTable1.getSelectedColumn()  == 8) {
            int row = jTable1.getSelectedRow();
            int idPartida = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
            try {
                estadistica.cargarJuegosCompetitivoDetalle(idPartida);
            } catch (SQLException ex) {
                System.out.println("Error al cargar detalle de partida competitiva!!!");
                System.out.println("Error: " + ex);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

