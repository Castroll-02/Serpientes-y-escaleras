    public Estadisticas(String opcion) {
        initComponents();
        this.setLocationRelativeTo(null);
        this.modalidad = opcion;
        
        jTable1.setDefaultRenderer(Object.class, new ImgTabla());
        
        try{
            switch(opcion){
            case "Jugador":
                lblTitulo.setText("ESTADISTICAS DEL JUEGO \"SERPIENTES Y ESCALERAS\" / DATOS DE LOS JUGADORES");
                cargarColumnas(tb,jugador);
                estadistica.cargarJugadores(tb);
                jTextField1.setVisible(false);
                btnBuscar.setVisible(false);
                lblIcono.setVisible(false);
                jTable1.setModel(tb);
                break;
                
            case "Individual":
                lblTitulo.setText("ESTADISTICAS DEL JUEGO \"SERPIENTES Y ESCALERAS\" / PARTIDAS INDIVIDUALES");
                cargarColumnas(tb,individual);
                estadistica.cargarJuegosIndividuales(tb, null);
                jTable1.setModel(tb);
                break;
                
            case "Competencia":
                tbx = (DefaultTableModel)jTable1.getModel();
                lblDetalle.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Recursos/detalle.png"))); 
                lblTitulo.setText("ESTADISTICAS DEL JUEGO \"SERPIENTES Y ESCALERAS\" / PARTIDAS DE COMPETENCIA");
                estadistica.cargarJuegosCompetitivo(tbx, null,lblDetalle);
                break;
                
            case "TOP3":
                lblTitulo.setText("ESTADISTICAS DEL JUEGO \"SERPIENTES Y ESCALERAS\" / TOP 3 JUGADORES COMPETENCIA");
                cargarColumnas(tb,top3);
                estadistica.cargarMejoresCompetencia(tb);
                jTextField1.setVisible(false);
                btnBuscar.setVisible(false);
                lblIcono.setVisible(false);
                jTable1.setModel(tb);
                break;
                
            default:
                System.out.println("Opcion no disponible");
                break;
        }
        } catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error al cargar los datos","Error",3);
            System.out.println("Mensaje de error: " + ex);
        }
        
        
    }
    
