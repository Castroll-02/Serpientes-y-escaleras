    public SeleccionFicha(String modalidad, String jugador,String seleccion) {
        initComponents();
        this.setLocationRelativeTo(null);
        this.modalidad = modalidad;
        this.jugador = jugador;

        switch(modalidad){
            case "Individual":
                break;
                
            case "Competencia":
                if ("1".equals(jugador)) {
                    jLabel1.setText("Seleccione la ficha para el jugador 1");
                } else {
                    jLabel1.setText("Seleccione la ficha para el jugador 2");
                    switch(seleccion){
                        case "1":
                            rSPanelBorder1.setEnabled(false);
                            btnFicha1.setEnabled(false);
                            break;
                       
                        case "2":
                            rSPanelBorder2.setEnabled(false);
                            btnFicha2.setEnabled(false);
                            break;
                            
                        case "3":
                            rSPanelBorder3.setEnabled(false);
                            btnFicha3.setEnabled(false);
                            break;
                            
                        case "4":
                            rSPanelBorder3.setEnabled(false);
                            btnFicha4.setEnabled(false);
                            break;
                    }
                }
                break;
            default:
                JOptionPane.showMessageDialog(null, "Error no hay modalidad seleccionada","Seleccione modalidad", 2);
                new MenuPrincipal().setVisible(true);
                this.dispose();
                break;
        }
    }
    
