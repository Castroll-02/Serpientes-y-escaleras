    private void btnFicha2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFicha2ActionPerformed
        if (modalidad.equals("Individual")) {
            Juego.setUrlFichaJ1("/Recursos/ficha-jett-tablero.png");
            Juego.setIdFicha1(2);
            
            if (Juego.getUrlTablero().equals("/Recursos/tablero_dificil.jpg")) {
                new PartidaHard(this.modalidad, "jett").setVisible(true);
            } else {
                new Partida(this.modalidad, "jett").setVisible(true);
            }     
            
            this.dispose();
        } else {
            if (jugador.equals("1")) {
                Juego.setUrlFichaJ1("/Recursos/ficha-jett-tablero.png");
                ficha1 = "jett";
                Juego.setIdFicha1(2);
                new SeleccionFicha("Competencia","2","2").setVisible(true);
                this.dispose();
            } else {
                Juego.setUrlFichaJ2("/Recursos/ficha-jett-tablero.png");
                ficha2 = "jett";
                Juego.setIdFicha2(2);     
                if (Juego.getUrlTablero().equals("/Recursos/tablero_dificil.jpg")) {
                    new PartidaCompetenciaHard(this.modalidad,ficha1,ficha2).setVisible(true);
                } else {
                    new PartidaCompetencia(this.modalidad,ficha1,ficha2).setVisible(true);
                }          
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnFicha2ActionPerformed

