    private void btnIniciarSesionCompetenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionCompetenciaActionPerformed
        Jugador.setClave(tool.encriptarClave(txtClave1.getText()));
        Jugador.setNickname(txtNickname1.getText());
        if (jugador.iniciarSesion()) {
            Jugador.setClave2(tool.encriptarClave(txtClave2.getText()));
            Jugador.setNickname2(txtNickname2.getText());
            if (jugador.iniciarSesionJugadorDos()) {
                JOptionPane.showMessageDialog(null, "Credenciales correctas", "Juego Competitivo", 1);
                new SeleccionTablero(modalidad).setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Credenciales del jugador 2 incorrectas ");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Credenciales del jugador 1 incorrectas");
        }
    }//GEN-LAST:event_btnIniciarSesionCompetenciaActionPerformed

