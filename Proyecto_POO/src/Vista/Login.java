    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
        Jugador.setClave(tool.encriptarClave(txtClave1.getText()));
        Jugador.setNickname(txtNickname1.getText());
        if (jugador.iniciarSesion()) {
            JOptionPane.showMessageDialog(null, "Credenciales correctas", "Juego Individual", 1);
            new SeleccionTablero(modalidad).setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Credenciales incorrectas");
        }
    }//GEN-LAST:event_btnIniciarSesionActionPerformed

